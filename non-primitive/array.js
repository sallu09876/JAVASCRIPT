// creating arrays

// 1.using array literals
const arr1 = [1, 2, 3, 4, 5];   
console.log(arr1); // [ 1, 2, 3, 4, 5 ]

// 2.using the Array constructor
const arr2 = new Array(6, 7, 8, 9, 10);
console.log(arr2); // [ 6, 7, 8, 9, 10 ]

// 3.creating empty arrays
let arr3 = [];
console.log(arr3); // []
arr3.push(11, 12, 13);
console.log(arr3); // [ 11, 12, 13 ]
