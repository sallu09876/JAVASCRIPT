// concat is an array method that is used to merge two or more arrays. This method does not change the existing arrays, but instead returns a new array.

const array1 = [1, 2, 3];
const array2 = [4, 5, 6];
const combinedArray = array1.concat(array2);
console.log(combinedArray); // Output: [1, 2, 3, 4, 5, 6]