let a = 10;
let b = 5;

console.log("a + b =", a + b);        // Addition: 15
console.log("a-b", a - b);            // Subtraction: 5   
console.log("a * b =", a * b);        // Multiplication: 50
console.log("a / b =", a / b);        // Division: 2
console.log("a % b =", a % b);        // Modulus: 0
console.log("a ** b =", a ** b);      // Exponentiation: 100000
console.log("a++ =", a++);            // Post-increment: 10 (then a becomes 11)
console.log("a after a++ =", a);      // Now a is 11
console.log("a-- =", a--);            // Post-decrement: 11 (then a becomes 10)
console.log("a after a-- =", a);      // Now a is 10
console.log("++a =", ++a);            // Pre-increment: 12
console.log("a after ++a =", a);      // Now a is 12
console.log("--a =", --a);            // Pre-decrement: 11
console.log("a after --a =", a);      // Now a is 11